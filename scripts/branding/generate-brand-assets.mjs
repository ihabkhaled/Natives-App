/**
 * Ultimate Natives brand asset generator.
 *
 * Reproducible ownership of derivatives from committed source art:
 *  - Verifies the committed source logo against its pinned checksum.
 *  - Text derivatives (brand-tokens.generated.css, favicon.svg,
 *    manifest.webmanifest) are produced by the typed generators in
 *    src/shared/design and asserted byte-for-byte by `npm run test`; this
 *    script reports that contract rather than duplicating it.
 *  - PWA raster icons use the exact Playwright/Chromium toolchain pinned for
 *    browser tests. Native assets still require @capacitor/assets or sharp and
 *    are reported as UNVERIFIED when that toolchain is absent.
 *
 * The authoritative checksum guard is src/shared/design/brand-source.constants.ts
 * and its colocated test; the value below is duplicated only so this standalone
 * script can run without the TypeScript toolchain.
 */
import { createHash } from 'node:crypto';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import process from 'node:process';

import { chromium } from '@playwright/test';

const SOURCE_PATH = 'public/brand-logo.png';
const EXPECTED_SHA256 = 'd5dc2db2b60df43eea7cf91dfb7f45fcfb5dc4e38d7692c1fe92b7980deb09cf';
const RASTER_TOOLS = ['@capacitor/assets', 'sharp'];
const PWA_ICONS = [
  { path: 'public/pwa-icon-192.png', size: 192, artworkScale: 1 },
  { path: 'public/pwa-icon-512.png', size: 512, artworkScale: 1 },
  { path: 'public/pwa-icon-maskable-512.png', size: 512, artworkScale: 0.7 },
];

function verifySource() {
  if (!existsSync(SOURCE_PATH)) {
    console.error(`Missing source art: ${SOURCE_PATH}`);
    process.exit(1);
  }
  const digest = createHash('sha256').update(readFileSync(SOURCE_PATH)).digest('hex');
  if (digest !== EXPECTED_SHA256) {
    console.error(
      `Source art checksum drift.\n  expected ${EXPECTED_SHA256}\n  actual   ${digest}`,
    );
    process.exit(1);
  }
  console.log(`Source art verified: ${SOURCE_PATH} (sha256 ${digest}).`);
}

function detectRasterTool() {
  return RASTER_TOOLS.find((name) => existsSync(`node_modules/${name}`)) ?? null;
}

async function renderPwaIcon(page, source, definition) {
  const base64 = await page.evaluate(
    async ({ imageSource, size, artworkScale }) => {
      const image = new globalThis.Image();
      image.src = imageSource;
      await image.decode();
      const canvas = globalThis.document.createElement('canvas');
      canvas.width = size;
      canvas.height = size;
      const context = canvas.getContext('2d');
      if (context === null) {
        throw new Error('Canvas 2D context unavailable');
      }
      context.fillStyle = '#000000';
      context.fillRect(0, 0, size, size);
      const artworkSize = Math.round(size * artworkScale);
      const offset = Math.round((size - artworkSize) / 2);
      context.drawImage(image, offset, offset, artworkSize, artworkSize);
      return canvas.toDataURL('image/png').split(',')[1];
    },
    {
      imageSource: source,
      size: definition.size,
      artworkScale: definition.artworkScale,
    },
  );
  if (base64 === undefined) {
    throw new Error(`PNG encoding failed for ${definition.path}`);
  }
  writeFileSync(definition.path, Buffer.from(base64, 'base64'));
}

async function generatePwaIcons() {
  const source = `data:image/png;base64,${readFileSync(SOURCE_PATH).toString('base64')}`;
  const browser = await chromium.launch({ headless: true });
  try {
    const page = await browser.newPage();
    for (const definition of PWA_ICONS) {
      await renderPwaIcon(page, source, definition);
    }
  } finally {
    await browser.close();
  }
  console.log(`Generated ${String(PWA_ICONS.length)} PWA icons from ${SOURCE_PATH}.`);
}

function reportRasterStep() {
  const tool = detectRasterTool();
  if (tool !== null) {
    console.log(
      `Raster toolchain "${tool}" detected. Regenerate native assets from ${SOURCE_PATH}.`,
    );
    return;
  }
  console.log('');
  console.log(
    'Raster + native icon/splash generation SKIPPED (UNVERIFIED): no image toolchain found.',
  );
  console.log(`Install one of [${RASTER_TOOLS.join(', ')}] and re-run to regenerate:`);
  console.log('  - Android adaptive/legacy launcher icons and splash screens');
  console.log('  - iOS AppIcon.appiconset and launch assets');
}

async function main() {
  verifySource();
  await generatePwaIcons();
  console.log('Text derivatives (brand CSS, favicon, web manifest) are generated by');
  console.log('src/shared/design/*.helper.ts and asserted byte-for-byte by "npm run test".');
  reportRasterStep();
}

await main();
