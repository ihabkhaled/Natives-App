import { createHash } from 'node:crypto';
import { existsSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { basename, extname, join, relative, sep } from 'node:path';
import process from 'node:process';

const DIST_DIRECTORY = 'dist';
const ASSET_DIRECTORY = join(DIST_DIRECTORY, 'assets');
const OUTPUT_PATH = join(DIST_DIRECTORY, 'service-worker.js');
const CACHEABLE_EXTENSIONS = new Set([
  '.css',
  '.gif',
  '.jpeg',
  '.jpg',
  '.js',
  '.png',
  '.svg',
  '.webp',
  '.woff',
  '.woff2',
]);

function walkFiles(directory) {
  return readdirSync(directory).flatMap((entry) => {
    const path = join(directory, entry);
    return statSync(path).isDirectory() ? walkFiles(path) : [path];
  });
}

function hasViteContentHash(path) {
  const extension = extname(path);
  if (!CACHEABLE_EXTENSIONS.has(extension)) {
    return false;
  }
  const stem = basename(path, extension);
  const hash = stem.slice(-8);
  const hasEncodedCharacter =
    hash !== hash.toLowerCase() ||
    [...hash].some((character) => character >= '0' && character <= '9') ||
    hash.includes('_') ||
    hash.includes('-');
  return (
    stem.length > 8 &&
    [...hash].every((character) => /[\w-]/u.test(character)) &&
    hasEncodedCharacter
  );
}

function toPublicUrl(path) {
  return `/${relative(DIST_DIRECTORY, path).split(sep).join('/')}`;
}

function buildIdFor(urls) {
  return createHash('sha256').update(urls.join('\n')).digest('hex').slice(0, 16);
}

function renderServiceWorker(urls) {
  const buildId = buildIdFor(urls);
  return `/* Generated by scripts/pwa/generate-service-worker.mjs. Do not edit. */
const PRECACHE_URLS = ${JSON.stringify(urls, null, 2)};
const STATIC_CACHE = 'ultimate-natives-static-${buildId}';
const OFFLINE_CACHE = 'ultimate-natives-offline-v1';
const OFFLINE_URL = '/offline.html';
const APP_SHELL_URL = '/index.html';
const CACHE_PREFIX = 'ultimate-natives-';
const NEVER_CACHE_PREFIXES = ['/api/', '/auth/'];
const PRECACHE_PATHS = new Set(PRECACHE_URLS);

self.addEventListener('install', (event) => {
  event.waitUntil(
    Promise.all([
      caches.open(STATIC_CACHE).then((cache) => cache.addAll(PRECACHE_URLS)),
      caches.open(OFFLINE_CACHE).then((cache) => cache.addAll([APP_SHELL_URL, OFFLINE_URL])),
    ]),
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(
          keys
            .filter(
              (key) =>
                key.startsWith(CACHE_PREFIX) &&
                key !== STATIC_CACHE &&
                key !== OFFLINE_CACHE,
            )
            .map((key) => caches.delete(key)),
        ),
      )
      .then(() => self.clients.claim()),
  );
});

self.addEventListener('message', (event) => {
  if (event.data?.type === 'SKIP_WAITING') {
    void self.skipWaiting();
  }
});

async function offlineNavigation(request) {
  try {
    return await fetch(request);
  } catch {
    return (
      (await caches.match(APP_SHELL_URL)) ??
      (await caches.match(OFFLINE_URL)) ??
      Response.error()
    );
  }
}

async function immutableAsset(request) {
  return (await caches.match(request)) ?? fetch(request);
}

self.addEventListener('fetch', (event) => {
  const request = event.request;
  if (request.method !== 'GET') {
    return;
  }
  const url = new URL(request.url);
  if (
    url.origin !== self.location.origin ||
    NEVER_CACHE_PREFIXES.some((prefix) => url.pathname.startsWith(prefix))
  ) {
    return;
  }
  if (request.mode === 'navigate') {
    event.respondWith(offlineNavigation(request));
    return;
  }
  if (PRECACHE_PATHS.has(url.pathname)) {
    event.respondWith(immutableAsset(request));
  }
});
`;
}

function main() {
  if (!existsSync(ASSET_DIRECTORY)) {
    console.error(`PWA worker generation failed: missing ${ASSET_DIRECTORY}. Run Vite first.`);
    process.exit(1);
  }
  const urls = walkFiles(ASSET_DIRECTORY).filter(hasViteContentHash).map(toPublicUrl).sort();
  if (urls.length === 0) {
    console.error('PWA worker generation failed: no content-hashed assets were found.');
    process.exit(1);
  }
  writeFileSync(OUTPUT_PATH, renderServiceWorker(urls), 'utf8');
  console.log(
    `PWA service worker generated: ${OUTPUT_PATH} (${String(urls.length)} immutable assets).`,
  );
}

main();
